    memcpy(font8x16[0x88], latvian_glyphs[9], 16);  // Ī
    memcpy(font8x16[0x89], latvian_glyphs[10], 16); // ī
    memcpy(font8x16[0x8A], latvian_glyphs[11], 16); // Ļ
    memcpy(font8x16[0x8B], latvian_glyphs[12], 16); // ļ
    memcpy(font8x16[0x8C], latvian_glyphs[13], 16); // Ņ
    memcpy(font8x16[0x8D], latvian_glyphs[14], 16); // ņ
