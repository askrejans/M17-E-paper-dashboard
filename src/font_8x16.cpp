void font8x16_init(void)
{
    memset(font8x16, 0, sizeof(font8x16));

    // ASCII 0x00-0x7F
    memcpy(font8x16, font8x16_basic, 128 * 16);

    // Extended glyphs
    for (unsigned i = 0; i < sizeof(glyph_map) / sizeof(glyph_map[0]); i++)
        memcpy(font8x16[glyph_map[i].slot], ext_glyphs[glyph_map[i].idx], 16);
}
