import os
from os.path import join

Import = None
try:
    # PlatformIO provides an Import function when running as an extra script
    from SCons.Script import Import
except Exception:
    Import = None

try:
    Import("env")
except Exception:
    pass

def read_env_file(env_path):
    values = {}
    if not os.path.isfile(env_path):
        return values
    with open(env_path, "r") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                k, v = line.split("=", 1)
                k = k.strip()
                v = v.strip().strip('"').strip("'")
                values[k] = v
    return values

def generate_secrets(source=None, target=None, env=None):
    root = os.getcwd()
    env_path = join(root, ".env")

    # Prefer actual environment variables, fallback to .env file
    ssid = os.environ.get("SSID", "")
    password = os.environ.get("PASSWORD", "")
    language = os.environ.get("LANGUAGE", "")
    date_format = os.environ.get("DATE_FORMAT", "")

    file_values = read_env_file(env_path)
    ssid = ssid or file_values.get("SSID", "")
    password = password or file_values.get("PASSWORD", "")
    language = language or file_values.get("LANGUAGE", "EN")
    date_format = date_format or file_values.get("DATE_FORMAT", "DD.MM.YYYY")

    include_dir = join(root, "include")
    os.makedirs(include_dir, exist_ok=True)
    secrets_path = join(include_dir, "secrets.h")

    with open(secrets_path, "w") as fh:
        fh.write("// Auto-generated by build/generate_secrets.py\n")
        fh.write("#pragma once\n")
        fh.write(f'#define WIFI_SSID "{ssid}"\n')
        fh.write(f'#define WIFI_PASSWORD "{password}"\n')
        fh.write(f'#define LANGUAGE "{language}"\n')
        fh.write(f'#define DATE_FORMAT "{date_format}"\n')
    print("[generate_secrets] Wrote:", secrets_path)


# If PlatformIO exposes `env` via SCons Import, try to register a pre-build action.
try:
    from SCons.Script import Import as _Import
    try:
        _Import("env")
        # If `env` is available in the module globals, register pre-action.
        if 'env' in globals():
            try:
                env.AddPreAction("$BUILD_DIR", generate_secrets)
            except Exception:
                pass
    except Exception:
        pass
except Exception:
    pass

# Also run generation at import time so `include/secrets.h` exists immediately.
generate_secrets()
